public class Study412 {
    public static void main(String[] args) {
        // 구구단
        for (int i = 2; i <= 7; i++) {
            for (int j = 1; j <= 3; j++) {
                System.out.println(i+"x"+j+"="+i*j);
            }
            System.out.println();
        }
    }
}
